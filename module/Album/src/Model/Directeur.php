<?php
namespace Album\Model;

use DomainException;
use Laminas\Filter\StringTrim;
use Laminas\Filter\StripTags;
use Laminas\Filter\ToInt;
use Laminas\InputFilter\InputFilter;
use Laminas\InputFilter\InputFilterAwareInterface;
use Laminas\InputFilter\InputFilterInterface;
use Laminas\Validator\StringLength;

class Directeur implements InputFilterAwareInterface
{
    public $id;
    public $nomDirecteur;
    public $prenomDirecteur;
    public $codeDirecteur;
    public $sexeDirecteur;
    public $adresseDirecteur;
    public $gradeDirecteur;
    public $domaineDirecteur;


    private $inputFilter;

    public function exchangeArray(array $data)
    {
        $this->id     = !empty($data['id']) ? $data['id'] : null;
        $this->nomDirecteur = !empty($data['nomDirecteur']) ? $data['nomDirecteur'] : null;
        $this->prenomDirecteur  = !empty($data['prenomDirecteur']) ? $data['prenomDirecteur'] : null;
        $this->codeDirecteur = !empty($data['codeDirecteur']) ? $data['codeDirecteur'] : null;
        $this->sexeDirecteur = !empty($data['sexeDirecteur']) ? $data['sexeDirecteur'] : null;
        $this->adresseDirecteur    = !empty($data['adresseDirecteur']) ? $data['adresseDirecteur'] : null;
        $this->gradeDirecteur = !empty($data['gradeDirecteur']) ? $data['gradeDirecteur'] : null;
        $this->domaineDirecteur  = !empty($data['domaineDirecteur']) ? $data['domaineDirecteur'] : null;
    
    }



    public function setInputFilter(InputFilterInterface $inputFilter)
    {
        throw new DomainException(sprintf(
            '%s does not allow injection of an alternate input filter',
            __CLASS__
        ));
    }

    public function getInputFilter()
    {
        if ($this->inputFilter) {
            return $this->inputFilter;
        }

        $inputFilter = new InputFilter();

        $inputFilter->add([
            'name' => 'id',
            'required' => true,
            'filters' => [
                ['name' => ToInt::class],
            ],
        ]);

        $inputFilter->add([
            'name' => 'nomDirecteur',
            'required' => true,
            'filters' => [
                ['name' => StripTags::class],
                ['name' => StringTrim::class],
            ],
            'validators' => [
                [
                    'name' => StringLength::class,
                    'options' => [
                        'encoding' => 'UTF-8',
                        'min' => 1,
                        'max' => 100,
                    ],
                ],
            ],
        ]);

        $inputFilter->add([
            'name' => 'prenomDirecteur',
            'required' => true,
            'filters' => [
                ['name' => StripTags::class],
                ['name' => StringTrim::class],
            ],
            'validators' => [
                [
                    'name' => StringLength::class,
                    'options' => [
                        'encoding' => 'UTF-8',
                        'min' => 1,
                        'max' => 100,
                    ],
                ],
            ],
        ]);

        $inputFilter->add([
            'name' => 'codeDirecteur',
            'required' => true,
            'filters' => [
                ['name' => StripTags::class],
                ['name' => StringTrim::class],
            ],
            'validators' => [
                [
                    'name' => StringLength::class,
                    'options' => [
                        'encoding' => 'UTF-8',
                        'min' => 1,
                        'max' => 100,
                    ],
                ],
            ],
        ]);

        $inputFilter->add([
            'name' => 'gradeDirecteur',
            'required' => true,
            'filters' => [
                ['name' => StripTags::class],
                ['name' => StringTrim::class],
            ],
            'validators' => [
                [
                    'name' => StringLength::class,
                    'options' => [
                        'encoding' => 'UTF-8',
                        'min' => 1,
                        'max' => 100,
                    ],
                ],
            ],
        ]);

        $inputFilter->add([
            'name' => 'sexeDirecteur',
            'required' => true,
            'filters' => [
                ['name' => StripTags::class],
                ['name' => StringTrim::class],
            ],
            'validators' => [
                [
                    'name' => StringLength::class,
                    'options' => [
                        'encoding' => 'UTF-8',
                        'min' => 1,
                        'max' => 100,
                    ],
                ],
            ],
        ]);

        $inputFilter->add([
            'name' => 'domaineDirecteur',
            'required' => true,
            'filters' => [
                ['name' => StripTags::class],
                ['name' => StringTrim::class],
            ],
            'validators' => [
                [
                    'name' => StringLength::class,
                    'options' => [
                        'encoding' => 'UTF-8',
                        'min' => 1,
                        'max' => 100,
                    ],
                ],
            ],
        ]);
         $inputFilter->add([
            'name' => 'adresseDirecteur',
            'required' => true,
            'filters' => [
                ['name' => StripTags::class],
                ['name' => StringTrim::class],
            ],
            'validators' => [
                [
                    'name' => StringLength::class,
                    'options' => [
                        'encoding' => 'UTF-8',
                        'min' => 1,
                        'max' => 100,
                    ],
                ],
            ],
        ]);

        $this->inputFilter = $inputFilter;
        return $this->inputFilter;
    }

    public function getArrayCopy()
    {
        return [
            'id'     => $this->id,
            'nomDirecteur' => $this->nomDirecteur,
            'prenomDirecteur'  => $this->prenomDirecteur,
            'codeDirecteur'  => $this->codeDirecteur,
            'gradeDirecteur' => $this->gradeDirecteur,
            'domaineDirecteur'       => $this->domaineDirecteur,
            'adresseDirecteur'  => $this->adresseDirecteur,
            'sexeDirecteur'  => $this->sexeDirecteur,
        ];
    }
}